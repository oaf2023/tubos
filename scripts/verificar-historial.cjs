const { PrismaClient } = require('@prisma/client')
const p = new PrismaClient()
;(async () => {
  const comps = await p.comprobanteHistorico.count()
  const items = await p.comprobanteItemHistorico.count()
  const ctas = await p.cuentaCorrienteMovimiento.count()
  const clientes = await p.cliente.count()
  const tubos = await p.cylinder.count()
  const vends = await p.usuario.count({ where: { rol: { nombre: 'vendedor' } } })
  console.log('=== VERIFICACIÓN POST-FASE 5 ===')
  console.log('Clientes:', clientes)
  console.log('Vendedores:', vends)
  console.log('Cylinders:', tubos)
  console.log('Comprobantes históricos:', comps)
  console.log('Items históricos:', items)
  console.log('Movs cta cte:', ctas)
  console.log('Total registros históricos:', comps + items + ctas)
  console.log('Gran total DB:', clientes + vends + tubos + comps + items + ctas)
  await p.$disconnect()
})()
